
function criptografa(tecla) {
	
	switch (tecla) {
	  case "a":
	  	return "ai"
	    break;
	  case "e":
	    return "enter";
		break;
	  case "i":
	    return "imes";
		break;
	  case "o":
	    return "ober";
		break;
	  case "u":
		return "ufat";
		break;  	
	  default:
	  	return tecla
	  	break;
	}
}

